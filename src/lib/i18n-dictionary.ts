export function getDictionary(locale: Locale) {
  return dictionaries[locale];
}

export function getRoleLabel(locale: Locale, role: Role) {
  return roleLabels[locale][role];
}

export function getStatusLabel(
  locale: Locale,
  value:
    | BookingStatus
    | BookingPaymentStatus
    | DisputeStatus
    | KycStatus
    | PaymentStatus
    | VehicleStatus
    | Role
    | string,
) {
  return statusLabels[locale][value] ?? String(value).replaceAll("_", " ");
}

export function getCategoryLabel(locale: Locale, value: VehicleCategory | string) {
  return categoryLabels[locale][value as VehicleCategory] ?? String(value);
}

export function getTransmissionLabel(locale: Locale, value: Transmission | string) {
  return transmissionLabels[locale][value as Transmission] ?? String(value);
}

export function getFuelTypeLabel(locale: Locale, value: FuelType | string) {
  return fuelTypeLabels[locale][value as FuelType] ?? String(value);
}

export function getPaymentMethodLabel(locale: Locale, value: PaymentMethod | string) {
  return paymentMethodLabels[locale][value as PaymentMethod] ?? String(value);
}

export function getDocumentTypeLabel(locale: Locale, value: DocumentType | string, role?: Role) {
  if (role === Role.OWNER && value === DocumentType.DRIVER_LICENSE) {
    return locale === "uz"
      ? "Avtomobil ro'yxatdan o'tkazish hujjati"
      : locale === "ru"
        ? "Документ о регистрации автомобиля"
        : "Vehicle registration document";
  }

  if (value === DocumentType.VEHICLE_REGISTRATION) {
    return documentTypeLabels[locale][value] ??
      (locale === "uz"
        ? "Avtomobil ro'yxatdan o'tkazish hujjati"
        : locale === "ru"
          ? "Документ о регистрации автомобиля"
          : "Vehicle registration document");
  }

  return documentTypeLabels[locale][value] ?? String(value);
}

export function getFeatureLabel(
  locale: Locale,
  value: "delivery" | "airportPickup" | "monthly" | "instant",
) {
  return featureLabels[locale][value];
}
